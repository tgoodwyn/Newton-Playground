    private void clampForce() {
        String input = ForceInput.getText();
        int massLimit = birdie.getMass() + 5;
        int val = 0;
        try {
            val = Integer.parseInt(input);
            if (val > 5000) {
                val = 5000;
            } else if (val < massLimit) {
                val = massLimit;
            }
            ForceInput.setText(String.valueOf(val));

        } catch (Exception e) {
            ForceInput.setText("600");
        }
    }
